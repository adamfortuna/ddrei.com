<p>* If you do not have 80% at this point, regardless of extras you will not qualify.<br><br>
If on the off shot someone gets 100pts from the judges before extras, they get an automatic bi to the final round.</p>

 
<p><b>Round 2:</b>
<ul style="list-style-type:decimal;">
<li>The remaining entries are bracketed again to adjust for Round 1</li>
<li>This time judges and the public will have direct say in what moves on
	<ul><li>For each bracket the public rank their Top 10</li>
	<li>For each bracket the judges rank their Top 10</li></ul>
</li>
<li>For both the judges and the public, from their list of 10, we are taking the TOP 3<br><br>
The judge picks take priority, so if the public picks any entry that is already selected by the judges, 
the entry directly below gets moved up.</li>
<li>Six (6) total entries per bracket will move on.</li>
</ul></p>

 
<p><b>Round 3: (Final)</b>
<ul style="list-style-type:decimal;">
<li>The remaining 6 entries (per bracket) are bracketed into final sets</li>
<li>The judges pick their top 4</li>
<li>The public picks their top 4</li></ul>
</p>

