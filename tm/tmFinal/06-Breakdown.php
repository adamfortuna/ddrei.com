<p><b>Round 1: Each Bracket will contain 50 entries</b>
<ul style="list-style-type:decimal;">
<li>All the entries are counted and checked for correct file formatting and errors</li>
<li>All entries are then randomly sorted into brackets</li>
<li>Each week a bracket will be released to the public</li>
<li>At the end of each week the results from the judges will be released.</li>
<li>The public will have no say in the first round. Public sway has been removed. </li>
</ul></p>

