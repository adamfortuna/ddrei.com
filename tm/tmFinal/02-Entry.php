<p align="center"><u>Please make sure your entry is formatted correctly!</u></p>

<ul style="list-style-type:decimal;">

<li>DWI is the required format; however, SM files are a welcomed addition.  The steps contained within the required DWI file are
the one that will be scored.  Bear in mind that no such judging criterion exists for the public, who will determine a large 
part of your final ranking. DWI and SM steps must be identical*; anyone caught compromising this rule will be immediately 
expunged from the contest.<br><br>
(We are aware some steps convert incorrectly out of SM, please note this in your Readme to avoid issues)</li>

<li>Final compressed size may not exceed 12mb. G-Mail has a 10mb file size limit, please split your file if there 
	is a complication.  Please contact Sivus with any issue you might have.</li>
	
<li>All Step Files <b>must</b> contain:
	<ul style="list-style-type:square;">
    	<li>Light Steps</li>
    	<li>Standard Steps</li>
    	<li>Heavy Steps</li>
	</ul>
</li>

<li>Backgrounds <b>must</b> be formatted as follows:
<p>Make sure you include a unique background: A background is a unique touch to any Step File and can be VERY 
important when it comes to making your Step File look sleek and professional. If you aren't the best artist in the world, 
just use <a href="http://images.google.com">images.google.com</a> to search for some artwork related to your song and fix it up to work with your entry. Note: 
If using artwork you didn’t make yourself, please mention its creator in your read-me file!</p>

	<ul style="list-style-type:square;">
    	<li>No Alpha channel</li>
    	<li>PNG or JPEG only</li>
    	<li>320 x 240 or 640 x 480</li>
	</ul>
</li>

<li>Entry <b>must</b> contain a banner.
<p>Your banner is likely the first thing that will be seen when the player gets to your Step File. Therefore, like the
background, this first impression can be important.  Banners <b>must</b> be formatted as follows:</p>

	<ul style="list-style-type:square;">
    	<li>No Alpha channel</li>
    	<li>PNG or JPEG only</li>
    	<li>256 x 80</li>
	</ul>
</li>


<li>Entry must contain either MP3 or OGG format file.
<p>If you have a sound file in another format, try looking for a free conversion program or just ask a friend who knows 
how to convert to do so for you.  Also, try to normalize your sound when converting so that it’s not too loud or too soft.</p>
	<ul style="list-style-type:square;">
		<li>MP3 or OGG must have  a bit stream greater than 128KBPS</li>
	</ul>
</li>



<li>Entry must contain a ReadMe file.
<p>Please take the time to know what you are using! If you use a theme, please use the full, correct name also.  
Failure to take this area seriously will lose you points. Please understand we’re trying to be professional about this. 
If you are entering a file into a contest you should be able to speak about, explain and justify your entry to the judges. 
We feel this is important.</p>

	<ul style="list-style-type:square;">
    	<li>Information used in your registration should be included in this file.</li>
    	<li>Please include the artist and give as much, accurate credit as possible.</li>
    	<li>Contact Info (E-Mail, AOL, MSN, or your DDRei User name for PM’s)</li>
		<li>Short write up about your entry</li>
		<li><b>No Fancy ANSI drawings</b></li>
	</ul>
</li>


</ul>


