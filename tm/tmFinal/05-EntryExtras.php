<p>As an added bonus to the total points you can earn for an entry, each of these will earn you a select bonus to your 
final score. Your total bonus score cannot exceed 5 pts; we do, however, encourage you to do as many of these extras as 
possible to both further your entry’s appeal and to increase your overall knowledge of complete file creation. While you 
can earn more than 5pts in this category, only 5 pts will be applied toward your final grade.</p>

<ul>
 
<li><b>Background Animations 1pts</b><br>
You may create background animations if you so desire.  However, all images must be in your entry folder and point to that in your STEP FILE.
</li>
 

<li><b>Movies 1pts</b><br>
You May include a movie with your entry, but it may not exceed 9mb. Must be in your entry folder and must point to that in your STEP FILE
</li>
 

<li><b>Challenge Steps 1pt</b><br>
If you want, you may include Challenge steps. However, they must be valid steps, meaning they must be doable on some level.
</li>
 

<li><b>Beginner Steps 1pt</b><br>
You may also include beginner steps in your entry. They should be easier than your Light steps but cannot be as simple as all lefts or rights.  Please put some effort into these.
</li>
 

<li><b>Doubles Steps 1-4 pts</b><br>
You may include double steps choosing difficulties from Light to Challenge, and we will award 1 point for each difficulty level that is well done.
</li>
 

<li><b>Solo Steps 1-4 pts</b><br>
Again, you may include solo steps choosing difficulties from Light to Challenge, if you wish to make them, and we will treat them in the same way as doubles, beginner, or challenge steps, i.e. lightly judge them to see if they deserve the bonus point(s).
</li>
 

<li><b>Lyric File 1pt</b><br>
You may also include a lyric file. Please make sure you are using the correct lyrics!
</li>

</ul>

